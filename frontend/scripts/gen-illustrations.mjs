// @ts-check
/**
 * gen-illustrations.mjs — the single source of truth for the illustration set.
 *
 * Reads the carried-over catalogue (public/illustrations/illustrations.json),
 * keeps only pieces whose file actually exists (this silently drops the stale
 * `brand/logo` entry — the logo was never carried over, see the library README),
 * measures each piece's intrinsic pixel size, and emits a typed manifest:
 *
 *   src/lib/illustrations/manifest.generated.ts
 *
 * It then VALIDATES that every id has bilingual alt text in messages/{en,fr}.json
 * under `Illustrations.alt.<category>.<name>` — so no illustration can ever ship
 * without an accessible, translated description (DESIGN §11; CLAUDE.md #5).
 *
 * Run:  npm run gen:illustrations   (or: node scripts/gen-illustrations.mjs)
 * Plain Node ESM — no extra dependency, no TS runner needed.
 */
import {readFileSync, writeFileSync, existsSync, mkdirSync} from 'node:fs';
import {dirname, join} from 'node:path';
import {fileURLToPath} from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, '..'); // frontend/
const publicDir = join(root, 'public');
const catalogue = JSON.parse(
  readFileSync(join(publicDir, 'illustrations', 'illustrations.json'), 'utf8')
);

/**
 * Intrinsic size of an SVG from its <svg> width/height, falling back to viewBox.
 * @param {Buffer} buf
 */
function svgDims(buf) {
  const head = buf.toString('utf8', 0, 800);
  const tag = head.match(/<svg[^>]*>/i)?.[0] ?? '';
  const w = tag.match(/\bwidth="([\d.]+)"/i);
  const h = tag.match(/\bheight="([\d.]+)"/i);
  if (w && h) return {width: Math.round(+w[1]), height: Math.round(+h[1])};
  const vb = tag.match(/viewBox="[\d.]+ [\d.]+ ([\d.]+) ([\d.]+)"/i);
  if (vb) return {width: Math.round(+vb[1]), height: Math.round(+vb[2])};
  return {width: 512, height: 512};
}

/**
 * Intrinsic size of a PNG from its IHDR chunk (width at byte 16, height at byte
 * 20, big-endian).
 * @param {Buffer} buf
 */
function pngDims(buf) {
  return {width: buf.readUInt32BE(16), height: buf.readUInt32BE(20)};
}

const items = [];
for (const it of catalogue.items) {
  const abs = join(publicDir, it.file);
  if (!existsSync(abs)) {
    console.warn(`  ↳ skipping ${it.id} — file not present (${it.file})`);
    continue;
  }
  const buf = readFileSync(abs);
  const dims = it.file.toLowerCase().endsWith('.png') ? pngDims(buf) : svgDims(buf);
  items.push({
    id: it.id,
    src: `/${it.file}`, // public/ → served at site root
    category: it.category,
    width: dims.width,
    height: dims.height
  });
}
items.sort((a, b) => a.id.localeCompare(b.id));

const categories = [...new Set(items.map((i) => i.category))].sort();
const sproutPoses = items
  .filter((i) => i.category === 'mascot')
  .map((i) => i.id.slice('mascot/'.length))
  .sort();

/** @param {readonly string[]} xs */
const u = (xs) => xs.map((x) => `'${x}'`).join(' | ');

const out = `// GENERATED by scripts/gen-illustrations.mjs — do not edit by hand.
// Run \`npm run gen:illustrations\` to refresh after changing the art set.
//
// The single typed catalogue every <Illustration> / <Sprout> reads. It carries
// only structural data (id, served path, category, intrinsic pixel size); the
// accessible ALT TEXT lives in next-intl messages so it is bilingual (EN/FR),
// keyed by id as \`Illustrations.alt.<category>.<name>\`.

export type IllustrationId =
  | ${items.map((i) => `'${i.id}'`).join('\n  | ')};

export type IllustrationCategory = ${u(categories)};

/** The Sprout mascot poses (the \`mascot/\` ids, prefix stripped). */
export type SproutPose =
  | ${sproutPoses.map((p) => `'${p}'`).join('\n  | ')};

export interface IllustrationMeta {
  /** Catalogue id, e.g. \`mascot/thinking\`. */
  readonly id: IllustrationId;
  /** Path served from /public, e.g. \`/illustrations/mascot/thinking.svg\`. */
  readonly src: string;
  readonly category: IllustrationCategory;
  /** Intrinsic pixel width — drives the aspect ratio (no layout shift). */
  readonly width: number;
  /** Intrinsic pixel height. */
  readonly height: number;
}

export const ILLUSTRATIONS: readonly IllustrationMeta[] = [
${items
  .map(
    (i) =>
      `  {id: '${i.id}', src: '${i.src}', category: '${i.category}', width: ${i.width}, height: ${i.height}}`
  )
  .join(',\n')}
];

export const ILLUSTRATION_BY_ID: Record<IllustrationId, IllustrationMeta> =
  Object.fromEntries(ILLUSTRATIONS.map((i) => [i.id, i])) as Record<
    IllustrationId,
    IllustrationMeta
  >;

export const ILLUSTRATION_CATEGORIES: readonly IllustrationCategory[] = [
  ${categories.map((c) => `'${c}'`).join(', ')}
];

/** Every Sprout pose, sorted — drives the typed \`<Sprout pose>\` prop. */
export const SPROUT_POSES: readonly SproutPose[] = [
  ${sproutPoses.map((p) => `'${p}'`).join(', ')}
];
`;

const outPath = join(root, 'src', 'lib', 'illustrations', 'manifest.generated.ts');
mkdirSync(dirname(outPath), {recursive: true});
writeFileSync(outPath, out, 'utf8');
console.log(`✓ wrote ${items.length} illustrations → src/lib/illustrations/manifest.generated.ts`);
console.log(`  ${sproutPoses.length} Sprout poses · ${categories.length} categories`);

// --- Validate bilingual alt coverage --------------------------------------
let missing = 0;
for (const locale of ['en', 'fr']) {
  const msgs = JSON.parse(readFileSync(join(root, 'messages', `${locale}.json`), 'utf8'));
  const alt = msgs?.Illustrations?.alt ?? {};
  for (const it of items) {
    const [cat, name] = it.id.split('/');
    if (typeof alt?.[cat]?.[name] !== 'string' || !alt[cat][name].trim()) {
      console.error(`  ✗ ${locale}: missing alt for ${it.id} (Illustrations.alt.${cat}.${name})`);
      missing++;
    }
  }
}
if (missing > 0) {
  console.error(`\n✗ ${missing} missing alt string(s). Add them to messages/{en,fr}.json.`);
  process.exit(1);
}
console.log('✓ every illustration has EN + FR alt text');
